//Examples
"5" == 5 // true because the value is 5 , the == is verifying only the value.
"5" === 5 // false because despite the value being 5 the type is different. One is a string and the other is a number.
"Florencia" === "Florencia"; //true both are strings. 
34 == 34 // true the value is the same. 
3500 == "3500" // true because the value is 3500 for both.
3500 === "3500" // false because one is a number and the other is a string.
