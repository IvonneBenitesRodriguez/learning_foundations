// Create a Function that calculates the power between 
// the voltage and the current
// It is a function remember to use the keyword return
// to get the result !!!
function circuitPower(voltage, current){
    return voltage * current;
}
console.log(circuitPower(10,30));
console.log(circuitPower(80,20));
console.log(circuitPower(55,11));
